package shapes;

import java.util.Comparator;

public class UncomparablePoint {

    private final double x;
    private final double y;

    public UncomparablePoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }



}
